public class pendahuluan_1 {

    public static void main(String[] args) {
        int a = 0;
        while (a < 10) {
            a++;
            System.out.println(a);
        }

        int b = 0;
        do {
            b++;
            System.out.println(b);
        } while (b < 10);

        int c = 0;
        for (int i = 0; i < 10; i++) {
            c++;
            System.out.println(c);
        }
    }
}
