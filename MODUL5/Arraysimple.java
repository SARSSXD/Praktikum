package MODUL5;

public class Arraysimple {
    public static void main(String[] args) 
    {
        int[] ages = new int[10];
        for( int i=0; i<ages.length; i++ )
        {
            System.out.print( ages[i] );
        }
        System.out.println(ages.length);//maksimal
    }
    
}
